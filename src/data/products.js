export function getProduct(slug) {
  return products.find(p => p.slug === slug) || null;
}

export function getRelatedProducts(currentSlug, limit = 4) {
  return products.filter(p => p.slug !== currentSlug).slice(0, limit);
}

export function getProductsByCategory(categorySlug) {
  if (!categorySlug || categorySlug === 'all') return products;
  if (categorySlug === 'sale') return products.filter(p => p.salePrice && p.salePrice < p.price);
  return products.filter(p => p.category === categorySlug);
}

export function sortProducts(list, sortBy) {
  const sorted = [...list];
  switch (sortBy) {
    case 'price-asc':
      return sorted.sort((a, b) => (a.salePrice || a.price) - (b.salePrice || b.price));
    case 'price-desc':
      return sorted.sort((a, b) => (b.salePrice || b.price) - (a.salePrice || a.price));
    case 'rating':
      return sorted.sort((a, b) => b.rating - a.rating);
    case 'newest':
      return sorted.sort((a, b) => (b.badge === 'new' ? 1 : 0) - (a.badge === 'new' ? 1 : 0));
    case 'bestseller':
    default:
      return sorted.sort((a, b) => (b.badge === 'bestseller' ? 1 : 0) - (a.badge === 'bestseller' ? 1 : 0) || b.reviewCount - a.reviewCount);
  }
}
